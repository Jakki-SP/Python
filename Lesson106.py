# list example
l1 = [10,20,30,40,50]
for x in l1:
    print(x)
print("-----------------")

# tuple example
t1 = (10,20,30,40,50)
for x in t1:
    print(x)
print("-----------------")

# dict example
d1 = {"idno":101,"name":"Ravi"}
for x in d1:
    print(x)
print("-----------------")

# str example
s1 = "python"
for x in s1:
    print(x)
print("-----------------")

# file example
f = open("sample.txt")
