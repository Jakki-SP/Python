# Example Programs on range Function
#===================================

for x in range(10):
    print(x,end=" ")
print("\nThanks")

# WAP to print 1 to 10 No's

for x in range(1,11):
    print(x,end=" ")
print("\nThanks")

# WAP to print Even no's from 80 to 60

for x in range(80,59,-2):
    print(x,end=" ")
print("\n Thanks")
